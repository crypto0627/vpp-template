/**
 * 檢查指定日期是否為台灣國定假日
 * @param dateString 日期字串，格式：YYYY-MM-DD (台灣時間)
 * @returns 是否為國定假日
 */
export function isTaiwanNationalHoliday(dateString: string): boolean {
  return TAIWAN_NATIONAL_HOLIDAYS.has(dateString);
}

/**
 * 檢查指定的 UTC Date 是否為台灣國定假日
 * @param utcDate UTC Date 物件
 * @returns 是否為國定假日
 */
export function isNationalHolidayTW(utcDate: Date): boolean {
  // 轉換為台灣時間並取得日期字串
  const tw = new Date(utcDate.getTime() + 8 * 60 * 60 * 1000);
  const year = tw.getUTCFullYear();
  const month = String(tw.getUTCMonth() + 1).padStart(2, "0");
  const day = String(tw.getUTCDate()).padStart(2, "0");
  const dateString = `${year}-${month}-${day}`;

  return isTaiwanNationalHoliday(dateString);
}
